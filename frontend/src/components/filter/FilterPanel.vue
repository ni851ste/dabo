<template>
    <div class="filter">

        <div class="filterOption">
            <b-input-group>
                <b-input
                        id="searchInput"
                        v-model="searchString"
                        type="text"
                        required
                        placeholder="Suche"
                ></b-input>
                <p>{{location}}</p>
            </b-input-group>

        </div>
        <hr/>
        <div class="filterOption">
            <label>
                <b-icon-geo-alt font-scale="1.2"></b-icon-geo-alt>
                Standort
            </label>
            <b-form-input
                    id="locationInput"
                    v-model="location"
                    type="text"
                    required
                    placeholder="Standort"
            ></b-form-input>
        </div>
        <hr/>
        <div class="categoryCheckboxes filterOption">
            <label>
                <b-icon-list-ul font-scale="1.2"></b-icon-list-ul>
                Kategorien
            </label>
            <b-form-group>
                <b-form-checkbox-group
                        v-on:click=""
                        id="category-checkbox-group"
                        v-model="selectedCategories"
                        :key="uuid"
                        :options="categories"
                >
                </b-form-checkbox-group>
            </b-form-group>
        <b-button class="applyFilter" v-on:click="getFilteredArticles()">
            <a class="nav-link disabled" href="#">
                <b-icon-funnel font-scale="1.2"></b-icon-funnel>
                Filter anwenden
            </a>
        </b-button>
        </div>
    </div>
</template>

<script lang="ts">
    import {Component, Prop, Vue} from 'vue-property-decorator';
    import Category from "@/components/category/Category";
    import $ from "jquery";
    import {v4 as uuid} from 'uuid'

    @Component
    export default class FilterPanel extends Vue {
        categories: Category[] = this.getCategories();

        getCategories(): Category[] {
            return Object.values(Category)
        }

        location: string = "";
        searchString: string = "";

        testCategories: Category[] = [Category.cars, Category.clothes];
        selectedCategories: Category[] = [];

        getFilteredArticles(): void {
            console.log("filter articles by ", this.testCategories);
            console.log("filter articles by ", this.selectedCategories);
            $.ajax({
                url: "http://localhost:9000/users/articles",
                type: "POST",
                data: JSON.stringify({
                    categories: this.testCategories,
                }),
                dataType: "json",
                contentType: "application/json",
                success: result => {
                    console.log("success ", result)
                },
                error: error => {
                    console.log("error ", error)
                }
            });
        }
    }

</script>

